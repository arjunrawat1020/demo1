							<!-- <div class="heading">Fuelling a Nation of Technically Skilled Professionals</div> -->
							<em>Develop a passion for learning. If you do, you will never cease to grow -
								<strong>Anthony J D’Angelo</strong></em><br>

							<p>Well…while growing from student to employee is a long journey. The journey may not be the
								same for every individual and every generation. Our way of imparting education and
								learning also have changed dramatically so much in last 3 decades, with advent of
								computers, Internet and its access in acquiring of knowledge and information on any
								topic through various sources have started disrupting our classroom learning experience.
							</p>
							<p>Today acquiring of knowledge & information about anything is possible through internet
								its access from one place to anyplace, from one time to anytime and anywhere, from one
								computer to multiple computers to tablets and mobile devices. It had started disrupting
								various businesses models and impact on economic outcomes and it going to impact
								education & learning in a huge way going forward. It is only going to increase its
								penetration and disrupt very age old ways of educating and learning.</p>
						</div>
						<div class="right"><img src="img/blog/future-skills-in-digital-era-large.png" alt=""></div>
					</div>

					<p>We at NASSCOM have been in several meetings with engineering college graduates, passed out
						graduate students undergoing trainings in private Institutes, IT Employees, L&D professionals,
						HR professionals talking about the need of enormous power of formalised informal learning and
						continuous learning platform.</p>
					<p>As we walk through out academic colleges, training institutions, & enterprise’s learning
						frameworks and talk with people about the need to expand their concept of learning, we received
						many positive interesting inputs and feedback on their experiences of learning and need for
						aggregator online learning platform.</p>
					<p>Future employees would no longer want to wait for the university to offer the right courses.
						While some autonomous institutes have started new subjects on new technologies, but they lack on
						relevant content, lab environment to develop the required skills on these technologies. They
						want to be able to get information online when they need it, at any place any time. They are
						keen in learning online about new technologies, while very few do register for such courses,
						most of them are not sure which course to register for on online. Top space where institutions
						are lacking are in – academic infrastructure, guidance, industry and practical exposure,
						foundation knowledge on new technologies, proper assessment certification, etc.</p>
					<p><strong>Challenges:</strong></p>
					<ul class="chlOp-links">
						<li><strong>35%</strong> feel their current curriculum structure does not support learning about
							futuristic technologies</li>
						<li><strong>61%</strong> preferred guided learning</li>
						<li><strong>87%</strong>feel foundation courses on new technologies in their undergraduate
							courses is necessary</li>
					</ul>
					<p><strong>Opportunities:</strong></p>
					<ul class="chlOp-links">
						<li><strong>85%</strong> say skills on new technologies will be needed in the future job market.
						</li>
						<li><strong>93%</strong> say better knowledge about these technologies will help them better
							anchor their caree</li>
						<li><strong>94%</strong> would prefer in self-learning</li>
						<li><strong>80%</strong> feel Institutions will recognize skill training based Internship</li>
						<li><strong>54%</strong> would prefer these courses to be an add-on over it being credit
							courses.</li>
						<li><strong>48%</strong> prefer blended mode of learning</li>
						<li><strong>71%</strong> prefer group learning</li>
						<li><strong>61%</strong> prefer guided learning</li>
						<li><strong>88%</strong> agree to the need of having aggregator skilling platform,
							certification, and curriculum sought out features</li>
						<li><strong>75%</strong> would like a subsidy based skilling ecosystem by Govt., Industry and
							academia</li>
					</ul>
					<p>As we move to understand our next set of audience, the passed out graduates who need jobs and
						their struggle to up-skill or become employable to the industry needs, every research report
						highlights on the employability quotient especially lack of their domain skills and softs skills
						relevant to industry needs. This is where the role of private training institutes becomes
						critical as suppliers of trained talent pool to the requirements of IT companies and Hyderabad
						is a thriving market for such institutions who also supporting reskilling of existing IT
						employees on the latest technologies. While most of them do register for online courses vast
						majority fail to complete those course do to varied reasons.</p>
					<p>We had an opportunity of interacting with both passed out graduates as well as IT employees who
						are currently pursuing traditional or basic programing course on C, C++, .net, java to high end
						courses like Data science, AI, ML, etc. to understand their preparedness on learning these new
						technology courses either online or offline and the following are some insights of our research
						on their informal & continuous learning needs.</p>
					<ul class="chlOp-links">
						<li><strong>42%</strong> feel they are preparing well for the Industry 4.0</li>
						<li><strong>68%</strong> say their training institute does not provide online mode of training
						</li>
						<li><strong>70%</strong> of IT Employees undergoing high end courses training feel their courses
							cover new technologies</li>
					</ul>
					<p><strong>Challenges:</strong></p>
					<ul class="chlOp-links">
						<li>Relevant Skill, Time, and Cost.</li>
						<li>Factors affecting completion of courses – Course duration, Relevant content, Not
							self-motivated.</li>
						<li><strong>62%</strong> and more say they were not successful in completing any online courses.
						</li>
						<li>Only <strong>49%</strong>say current curriculum structure will be able to support incoming
							futuristic tech.</li>
					</ul>
					<p><strong>Opportunities:</strong></p>
					<ul class="chlOp-links">
						<li><strong>68%</strong> feel the need to have an aggregator platform which provides skilling on
							Futuristic technology in India, driven by Govt., Industry, and Academia</li>
						<li><strong>41%</strong> feel this platform should offer classroom based training; while 38% go
							for blended platform</li>
						<li><strong>64%</strong> think that informal ways of learning can impact the overall learning of
							futuristic technology</li>
					</ul>
					<p>Existing employees, new hires, and management trainees, though have access to learning through
						LMS or through online MOOCS courses and reference materials, lack information published online
						on day to day basis in form of research papers, used cases, virtual labs, assessments etc. for
						constant reskilling & up-skilling to stay relevant in ever changing business needs. Existing
						employees experience ranging from 4 month to 16 years are part of this research and our
						learnings are highlighted below.</p>

					<ul class="chlOp-links">
						<li><strong>76%</strong> of them are currently working on Digital Technologies, like
							Microservers, Cyber, Cloud, AI, ML, IoT, etc.</li>
						<li><strong>82%</strong> of them agree that more advanced technology can be used to do their
							work.</li>
						<li><strong>91%</strong> of them feel the need to upgrade or up-skill on these futuristic
							technology to stay relevant in the future.</li>
					</ul>
					<p><strong>Challenges:</strong></p>
					<ul class="chlOp-links">
						<li>Time, Relevant courses, and cost</li>
						<li><strong>82%</strong> agree that both technical and soft skills will play vital role in
							performance of their job in future.</li>
						<li><strong>56%</strong> say they do not get any lab environment to practise.</li>
						<li><strong>56%</strong> say they choose time beyond office hours if they want to up-skills.
						</li>
					</ul>

					<p><strong>Opportunities:</strong></p>
					<ul class="chlOp-links">
						<li><strong>50%</strong> prefer blended learning;</li>
						<li><strong>32%</strong>
							prefer <strong>online</strong> learning; <strong>17%</strong> prefer <strong>classroom</strong> based
							learning.</li>
						<li><strong>79%</strong> show interest in learning new technology</li>
						<li><strong>88%</strong> would prefer investing in self-learning</li>
						<li><strong>97%</strong> agree that an aggregator platform to skill is needed in India and
							should offer blended mode of learning</li>
						<li><strong>100%</strong> say that platform (and if it offers certificate) should be driven by
							Government, Industry, and Academia.</li>
					</ul>
					<p>The business environment in the last 5-10 years has changed dramatically. According to Gartner
						report.</p>
					<p>87% of business leaders, digitization is a top priority. These changes have led to three
						important demands for learning: Learning must be available
						<strong><em>“on-demand,”</em></strong> it must be <strong><em>“job-relevant,”</em></strong> and
						it must be <strong><em>“constantly changing.”</em></strong> These new drivers are making the
						existing ways of learning in corporate or university models impossible to maintain pace with
						constantly changing Industry needs</p>
					<p>Today’s growing economy needs a constant supply of skilled manpower relevant to companies. But
						our emphasis on degree rather than a skill is stumbling block in the way of economic growth. On
						one hand India has a large number of educated and unemployed graduates, on the other hand
						Industry is desperately short of skilled professionals. In the wake of the changing economic
						environment it is necessary to focus on inculcating and advancing the skill sets, especially
						Digital Technology skills to the young population of the country.</p>
					<p>To address the above challenges there are online learning experience platforms - it exists
						because the paradigm of the learning management system is getting out dated. People no longer
						search for courses the way they used to we need a way to train and learn “in the flow of work”
						or learning in the flow.</p>

					<ul class="keyCap_list">
						<p>What this means is that LXP system has a set of key capabilities.</p>
						<li>They accommodate any form of content, including articles, podcasts, blogs, micro learning,
							videos, and courses</li>
						<li>They present content in “Netflix-like” interface, with recommendations, panels, mobile
							interfaces, and ML driven recommendations</li>
						<li>They are social, and they include social profiles which connect content to people to create
							authority</li>
						<li>They have paths or learning track or trails so you can follow content to logical learning
							outcome</li>
						<li>They have some form of assessment and often badging or certification.</li>
						<li>They make it easy to publish your own content as an individual.</li>
						<li>They are mobile, fun to use, fast and easy to traverse and have great search and embedded
							learning features.</li>
					</ul>
					<p>As we understand from above insights through various stakeholders the beginning of a new wave of
						learning experience is only going to increase and as you search for your next learning platform
						make sure you look around for the options which are clearly expanding. Futureskills portal is
						one such experience which will help the Indian IT-ITeS industry in reskilling/upskilling.
						Futureskills phase 2 will also be available to students in near future.</p>

