							<p>The theory of “Natural Selection”, as specified by the 19th century modern biologist
								Darwin, also used as a synonym to “survival of the fittest”, coined by Herbert Spencer
								to draw comparison between the economic theory and the biological one, is the process of
								preservation of favored races in their struggle for life. Who is really favored in this
								race? Darwin explains -</p>
							<p>“This preservation, during the battle of life, of varieties which possess any advantage
								in structures, constitution, or instinct. I have called Natural selection; and Mr.
								Herbert Spencer has well expressed the same idea by the survival of the fittest.” [1]
							</p>
							<p>As this race navigates in different spheres of life, it can easily abstract the theory of
								evolution/ natural selection/ survival of fittest into multiple different fields –
								mathematics, chemistry, computer science, IT and pretty much every other field which
								studies changes over time. </p>
						</div>
						<div class="right"><img src="img/blog/the-urgent-need-to-skill-large.png" alt=""></div>
					</div>
					<p>The 4th industrial revolution is around the corner with a wave of technologies[2] (Artificial
						Intelligence, Big Data, Mobile computing, Cloud Computing, Robotics and Automation, etc.)
						knocking the door of every industry such as Financial sector, education, health, agriculture,
						energy, infrastructure, government service delivery system, etc. The changes predicted by this
						wave is projected to be at an exponential rate, whose affects will not be localized but
						globalized. India is at a stature of having the youngest population in the ageing world, By
						2020, the median age in India will be just 28, compared to 37 in China and the US, 45 in Western
						Europe, and 49 in Japan[3]. Demographics can change the pace and pattern of economic growth.
						While China’s spectacular growth has already benefited from a demographic dividend, India is yet
						to do so. But is the young population ready to sail in the tide of change, did it adapt well to
						the changing tide, are they the fittest in this journey to cope up with change, and will they
						survive in the changing structures and constitutions?</p>
					<p><strong>History</strong></p>
					<p>19th century Britain saw rise of the secret oath based radical organization of English textile
						workers - Luddites, who destroyed factories and machines as a form of protest against the 1st
						Industrial revolution to protect their livelihood. Their protest was designed against layoffs
						and business owners who replaced them with machines. This story narrates the shadow that
						automation or change creates, which generates fear of mass joblessness and loss of
						livelihood.[4][5][6]</p>
					<p>In the 1980s, the age of personal computers created a wave of “Computer phobia”[7], which saw
						people depicting a range of resistance, fears, anxieties, and hostilities. The fear erupting out
						of the feeling that you can be replaced by a machine, become slave to it, or feeling aggressive
						towards computers. This moved into the 1990s creating another technological anxiety – the cyber
						space. [8]</p>
					<p><strong>Present -</strong></p>

					<p>The previous three revolutions came with their own anxieties, job scare, livelihood issue, skills
						being irrelevant, etc. The fourth revolution has its own features and is totally unique in its
						characteristics – being exponential in growth and not localized to any industry. McKinsey Global
						Institute report suggests that by 2030 intelligent agents and robots could eliminate as much as
						30% of world’s human labor. Automation will displace between 400 and 800 million jobs by 2030,
						requiring as many as 375 million people to switch job categories entirely[9][10]. The impending
						civil chaos does not seem distant. The executives of top companies put skilling in their top 10
						priorities to train their existing workforce for the up tide, so that they stay relevant. [11]
					</p>
					<p>Prime Minister Modi’s dream is to make India the global capital of skilled resource. He also
						pointed out that demographic dividend will become an issue to tackle if not dealt well[12]. For
						this, extensive skilling is needed to tackle global challenges. While the government has taken
						policy decisions to address some skilling challenges – Skill India, PMKVY (Pradhan Mantri
						Kaushal Vikas Yojana), RPL (Recognition of Prior Learning), etc. These schemes do not address
						the upcoming hurricane of disruptive technologies. India today falls behind to provide a common
						platform to train its young population on futuristic technology. These technologies will require
						a higher cognitive capability and effort that will require time and a lot of energy. Extensive
						planning and policy initiatives needs to be in place for this to answer this urgent need to
						skilling and up skilling people.</p>

