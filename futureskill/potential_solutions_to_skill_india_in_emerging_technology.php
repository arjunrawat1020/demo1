							<!-- <div class="heading">Potential Solutions to Skill India in Emerging Technology</div> -->
							<p>In 4 years, India will have the world’s largest population of working people, about 87
								crore in all[1]. India’s capability to utilize the opportunity that the projected
								demographic dividend is going to give is not assured. Our labor force or working
								population, the enhancers of GDP, will be outgrowing the number of people who depend on
								the GDP. According to a paper written by Shekar Aiyar and Ashok Mody[2], it is projected
								that over next two decades in India, the increasing demographic dividend could add 2%
								points per annum to India’s GDP growth. However, this number is dependent on the
								efficient policy decisions to provide a solid base for this number, and lack of such
								policies will turn the demographic dividend into a big disaster. One such policy
								investment is skilling India[3]. The present government realizes and recognizes the
								problem and is investing in initiatives to equip millions but at a very lower cognitive
								level of jobs. The real question is, do we have enough policy infrastructure which will
								skill India in the emerging technologies?</p>
						</div>
						<div class="right"><img src="img/blog/potential-solutions-to-skill-india-in-emerging-technology-large.png" alt=""></div>
					</div>

					<p>Additionally, the job scenario in IT Industry is undergoing a transformation due to adoption of
						automation and emergence of newer technologies (including disruptive technologies). Due to this,
						new job roles requiring different skill-sets are appearing and earlier jobs based on older
						skill-sets are losing their relevance. It has been indicated by NASSCOM that the total employee
						base of the IT-BPM Industry in India (as on FY 2017-2018) is around 3.9 million. However, due to
						growing automation, emergence of disruptive as well as new technologies, etc., about 30% of the
						existing workforce would require re-skilling to stay relevant and about 50-60% of the workforce
						would require re-skilling on continual basis.</p>

					<p>The solution for skilling has to be multi-pronged and can expand to multiple spheres of society
						and economy. The target audience that needs to be skilled can have its own exclusive skilling
						mechanism to train. The expansive target audience can be – student’s part of the high school
						ecosystem, students graduating in STEM courses, pool of students graduating and searching for
						jobs, IT/ITeS sector employees, etc. Based on the target audience, there are probable solutions
						that we can explore –</p>
					<p><strong>Educational Institutions (High School)</strong>– Catch them when they are young    
						India has a large pool of students graduating from school. Their curriculum revolves around the
						subjects like Science, Commerce, and Humanities. The target industry for skilling India should
						focus a lot on the budding population that will come out of schools and will enter colleges. To
						train them on the foundations of futuristic technology early in their life will help them in
						taking advance studies revolving on subjects whose future we are certain will be more complex.
						National level initiatives to propose skilling policy can include a month long training on the
						Foundations of futuristic technology or probably open incubation centers in each school and
						promote the technologies to inculcate participatory learning from students to self-invest in
						their learning.</p>
					<p><strong>Higher Education (STEM Institutes)</strong>- According to a report published by UN
						Conference on Trade and Development (UNCTAD), Technology and Innovation Report[4], India leads
						the world in the number of students getting bachelor’s degree in Science, Technology,
						Engineering, and Mathematics (STEM) subjects. The UGC reports, in 2016-2017, 10.7 million
						students were studying science, engineering/ technology or computer science. UNCTAD stressed on
						the rapid technological progress which will require to skill the labor force on broader range of
						skills, to secure livelihood and increase employability. UNCTAD warned that the educational
						establishments were not keeping pace with technological advances during the current transition
						period. This requires the educational institutions to revamp their core curriculum structure to
						also include the foundational and advanced courses on futuristic technology which students can
						opt for during their years of higher education. A collaborative central and state effort is
						required to provide awareness to these educational institutes on the imminent threat of these
						technologies, and the importance of making sure their students are ready before entering the
						work force.</p>
					<p><strong>Learning Systems within Industry</strong>- Industry is increasingly realizing the need to
						reskill/ upskill their existing workforce on new technologies for them to stay relevant for new
						job roles that are coming up with this wave. They are investing in doing deep diagnostics to
						learn about the status of their workforce and designing suitable career pathway for them to
						pursue. They are building extensive learning ecosystem to continuously train their employees on
						new skills and technologies. Since it will be challenging to create/maintain physical training
						infrastructure to match with the emerging technical requirements due to the high rate of
						technological obsolescence coupled with faster peaking of newer technologies appearing on the
						horizon, practically within about 6-12 months’ time. Also keeping in view that the working
						professionals may have difficulty to attend regular class-room based training programs, hence,
						offering them with an additional choice of on-line platforms for learning with multiple skilling
						options would be a better choice to encourage any-time, any-where, self-paced learning for
						acquiring newer and Industry relevant skill-sets. There are multiple online learning ecosystems
						which are coming up to provide the space to easily learn new technology, however the completion
						rate of courses, as reported by these platforms, is very low.  Industry collaboration is needed
						to create a common resource pool for the employees to benefit from, and create rewarding system
						so a self-participatory life cycle is created.</p>
					<p>To cater to these requirements, a ‘Re-Skilling/ Up-Skilling Framework – FutureSKills’ platform
						which is technology enabled, for the employees of IT sector, was set up by an Industry
						initiative. Futureskills platform for the B2B skilling was launched by the Prime Minister of
						India in February 2018, and it provides an online ecosystem and marketplace for industry to
						engage in skilling journey of India</p>
					<p><strong>Government created learning spaces</strong>- Government too has realized the need to
						Skill India, with schemes like Skill India, DDU-GKY, PMKVY etc. launched this term to address
						the mass skilling need. However these schemes do not focus on any of the disruptive futuristic
						technology. Government’s policy architecture needs a shift towards creating spaces to skill its
						large population on technology which will change the landscape of every industry. To even
						realize the billion dollar dream from this industry, the catalyst behind it needs to be skilled
						and well trained. Also, there is an urgent need to create a robust digital infrastructure to
						realize and enable the existing skilling dream.</p>
					<p><strong>Public Private Partnership Model – Government + Industry + Academia</strong>- The rapid
						pace of the 4th industrial revolution will disrupt every industry. Localized efforts will not be
						able to reap a lot of benefits from the upcoming trend. Therefore a totally new idea is needed
						to create a huge skilling ecosystem. The three institutions – Government, Industry, and Academia
						needs to join hands together to create this infrastructure. This mechanism needs to start from
						the scratch – from building industry verified curriculum, creating and using course aligned to
						the curriculum, developing a platform to propagate the content, analyzing the end result of the
						learning through assessment, etc. This framework will seek to maximize learning and
						participation by creating a one stop solution for Indians to understand, learn, and skill
						themselves on the new wave of technology, and be ready to adapt with the changing world.</p>
					<p>Hence, my idea of an ideal scenario would be that the Government of Indian must supplement the
						efforts of Industry by strengthening and institutionalizing the framework from both the sides –
						supply as well demand. A joint action by Government, Industry and Academia, as Team India
						Model, for Re-Skilling & Capacity Building will be the best solution.</p>

